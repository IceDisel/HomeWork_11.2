def get_upper_str(string):
    """
    Функция переводит слово в верхний регистр
    :param string:
    :return:
    """
    return string.upper()


def get_title_str(string):
    """
    Функция делает каждое слово с заглавной буквы
    :param string:
    :return:
    """
    return string.title()
